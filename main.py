import pandas as pd

def calculate_financial_ratios(data):
    """
    حساب النسب المالية الأساسية بناءً على مدخلات القوائم المالية
    """
    ratios = {}
    
    # 1. نسب الربحية
    ratios['هامش صافي الربح (%)'] = round((data['صافي الربح'] / data['الإيرادات']) * 100, 2)
    ratios['العائد على الأصول ROA (%)'] = round((data['صافي الربح'] / data['إجمالي الأصول']) * 100, 2)
    ratios['العائد على حقوق الملكية ROE (%)'] = round((data['صافي الربح'] / data['إجمالي حقوق الملكية']) * 100, 2)
    
    # 2. نسب السيولة
    ratios['نسبة التداول'] = round(data['الأصول المتداولة'] / data['الالتزامات المتداولة'], 2)
    ratios['رأس المال العامل'] = round(data['الأصول المتداولة'] - data['الالتزامات المتداولة'], 2)
    
    # 3. نسب المديونية والرفع المالي
    ratios['الدين إلى حقوق الملكية D/E'] = round(data['إجمالي الالتزامات'] / data['إجمالي حقوق الملكية'], 2)
    ratios['نسبة الملكية (%)'] = round((data['إجمالي حقوق الملكية'] / data['إجمالي الأصول']) * 100, 2)
    
    # 4. مؤشرات التقييم
    ratios['مكرر الربحية P/E'] = round(data['سعر السهم'] / data['ربحية السهم EPS'], 2)
    ratios['مكرر القيمة الدفترية P/B'] = round(data['سعر السهم'] / data['القيمة الدفترية للسهم'], 2)
    ratios['عائد التوزيعات (%)'] = round(data['التوزيعات للسهم'] / data['سعر السهم'] * 100, 2)
    
    return ratios

